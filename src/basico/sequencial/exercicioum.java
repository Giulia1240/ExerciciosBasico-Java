package basico.sequencial;

public class exercicioum {

	public static void main(String[] args) {
		/*
		 * Exercício 01 Faça um programa para ler dois valores inteiros, e depois
		 * mostrar na tela a soma desses números com uma mensagem explicativa, conforme
		 * exemplos. Exemplos:
		 * 
		 * Entrada: 10 30 Saída: SOMA = 40
		 * 
		 * Entrada: -30 10 Saída: SOMA = -20
		 * 
		 * Entrada: 0 0 Saída: SOMA = 0
		 */

		int a = 10, b = 30, c = -30, d = 10, e = 0, f = 0;

		int soma = a + b;
		int somaDois = c + d;
		int somaTres = e + f;

		System.out.println(soma);
		System.out.println(somaDois);
		System.out.println(somaTres);

	}
}
